        <p>So with the discussion of the <em>safety of Puerto Vallarta and the crime rate of Puerto Vallarta </em> out of the way here is our picks for <strong>the 5 best things to do in Puerto Vallarta.</strong></p>
        <h2><strong>the 5 best things to do in Puerto Vallarta</strong></h2>

        <p>So you’ve found yourself with the dilemma of choosing a vacation spot for your next trip and you’ve chosen P.V. as your spot, now what? Luckily you’re in the right place for a little overview on a few spots that have been a popular choice for many tourists, bear in mind that this is the first post of a series of <strong>“things you must see in Puerto Vallarta”</strong> so let’s dive in!</p>

        <h3><strong>1.	Dowtown Puerto Vallarta and Olas Altas</strong></h3>
        <p>Don’t let the two different names fool you, you can easily reach the two places at once just by following the <strong>Malecon Boardwalk</strong>.</p>
        <p><strong>Downtown Puerto Vallarta</strong> is a vibrant place with many places to explore, eat and drink a perfect place to explore the essence of what made Puerto Vallarta so appealing for golden age of Hollywood stars like <strong>Elizabeth Taylor</strong>.</p>
        <p>This place combines the rural and colonial feeling of Puerto Vallarta dotting it with a modern look as you see the big buildings and luxury restaurants all over the place, there’s also a big scene of the LGBT community int this area which gives this place a joyful and colorful feeling.</p>
        <p>While you’re around this area don’t forget about the <strong>“Our Lady of Guadalupe” </strong>church that lies at the hearth of the town.</p>
        <p>Also don’t forget to read all <strong>the Malecon boardwalk poems</strong> laying on the ground!</p>

        <h3><strong>2.	Olas Altas Pier</strong></h3>
        <p>While this place technically lies on the previous location, it deserves a whole other mention due to the significance of this place for the people of Puerto Vallarta.</p>
        <p>This pier was used for transporting goods to the set of <strong>“The Night of the Iguana”</strong> and served the local fishermen for many years until it was demolished and rebuilt by the architect José de Jesús Torres Vega and appeared on the CNN’s top <a href="https://cnnespanol.cnn.com/2014/07/28/los-9-muelles-mas-hermosos-del-mundo/"><strong>“Los 9 muelles más hermosos del mundo”</strong></a> (The 9 most beautiful piers in the world).</p>
        <p>This pier is indeed a must visit on <strong>any trip you make to Puerto Vallarta</strong> or nearby, also there’s lots of restaurants and coffee places that will surely delight you.</p>
            <img src="assets/img/vallarta-muelle.jpg" class="img-rounded img-responsive" alt="Puerto Vallarta Pier">

        <h3><strong>3.	Yelapa</strong></h3>
        <p>While not inside of Puerto Vallarta, you can easily reach <strong>Yelapa</strong> through the <strong>Olas Altas Pier </strong>mentioned above.</p>
        <p><strong>Yelapa is an important destination to visit while you’re in Puerto Vallarta</strong>, this beautiful wild and isolated place is perfect for people that just want to relax and forget about everything for just a bit.</p>
        <p><strong>Yelapa is home of artist and painters</strong>, so don’t be surprised if you find a colorful place and eccentric expats.</p>
        <p>Reaching Yelapa is easy enough, <strong>first you’ll need a boat, you can’t reach Yelapa without one as there is no roads leading this place </strong>(we meant it when we said it was isolated).</p>
        <p>And secondly you’ll need someone local to show you around, luckily you’re in the right place for that, <a href="https://vikingocharters.com/contact"><strong>contact us</strong></a> for our special rates and take a peek at our <a href="https://vikingocharters.com/tour-yelapa"><strong>tour page</strong></a>.</p>
        <p>All in all, Yelapa is an important place to visit, this place also receives hundreds of tourist every year and for good reason, this place is gorgeous and filled with adventure.</p>
