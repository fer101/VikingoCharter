        <p>Going to Puerto Vallarta this summer? Or just trying to find a good place to visit just to spend a few days, it doesn’t matter here we’re going to help you find out the 5 best things to do in Puerto Vallarta.</p>
        <hr>
        <p>First of all, if you have any concern about the <a href="https://www.numbeo.com/crime/in/Puerto-Vallarta">safety of Puerto Vallarta</a>,or the crime rate of Puerto Vallarta just rest assured that this city is really safe to visit, with a crime rate much, much lower than big cities in the U.S. so rest assured that if you follow a basic exercise of common sense you’ll be fine.</p>
