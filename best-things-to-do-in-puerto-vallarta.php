        <h3><strong>4.	Los Arcos National Marine Park</strong></h3>
        <p>This an extremely requested place for many tourists both national and international <strong>Los Arcos National Marine Park</strong> (often referred to just Los Arcos to avoid headaches) is a perfect addition to your schedule, perfect for families and eco tourists this place offers great views and photo opportunity.</p>
        <p><strong>Los Arcos National Marine Park</strong> is a protected area due to their huge variety of tropical fish and bird life, so if you’re planning on diving a little bit (which is totally permitted) please try to bring a sunscreen lotion that is bio friendly <a href="https://oceanservice.noaa.gov/news/sunscreen-corals.html"><strong><em>(fish don’t seem to like heavy chemicals and Nano-zinc oxide who knew, eh?)</em></strong></a>.</p>
        <p>Of course what you’ll need to visit this place is a boat and maybe some snorkeling gear if you want to swim and dive a bit, <a href="https://vikingocharters.com/tour-arcos"><strong>luckily we offer both at our Los Arcos tour</strong></a></p>
            <img src="assets/img/los-arcos.jpg" class="img-rounded img-responsive" alt="Los Arcos National Park">

        <h3><strong>5.	Fishing</strong></h3>
        <p>As a one of <strong>Puerto Vallarta sport fishing charters</strong> of course we are going to recommend <strong>fishing in Puerto Vallarta</strong>, this a good place to fish all year long for both big game and small game fishing.</p>
        <p><strong>Fishing in Puerto Vallarta</strong> is easy, basically <strong>during summer you have the Mahi season and in October and November you have the Marlin and sailfish season</strong>, all year long you have the Jacks and Bonitas, both fish are great for eating.</p>
        <p><strong>There’s also tournaments and many competitions all year long</strong>, there’s also a huge opportunity for amateur fishermen and people that never have been fishing due to the difficulty range of our waters.</p>
        <p>As for veteran fishermen and pros there’s of course <strong>the offshore fishing in Puerto Vallarta</strong> that is filled with bill fish and more.</p>
